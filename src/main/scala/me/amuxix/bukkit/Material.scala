    override def aetherize: AetherMaterial = bukkitMaterialToMaterial.getOrElse(material, throw new Exception(s"${material.name()} is missing its aether form"))
  }

  implicit class MaterialOps(material: AetherMaterial) extends BukkitForm[BukkitMaterial] {
    override def bukkitForm: BukkitMaterial = materialToBukkitMaterial.getOrElse(material, throw new Exception(s"${material.name} has no matching bukkit form"))
  }
